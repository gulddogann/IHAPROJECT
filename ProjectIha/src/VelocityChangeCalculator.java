public class VelocityChangeCalculator {

    public static double calculateVelocityChange(double initialVelocity, double finalVelocity) {
        // initialVelocity: Başlangıç hızı (örneğin, m/s)
        // finalVelocity: Bitiş hızı (örneğin, m/s)

        // Hızın değişimi hesaplama formülü: Değişim = Bitiş Hızı - Başlangıç Hızı
        double velocityChange = finalVelocity - initialVelocity;

        return velocityChange;
    }

}


